	<!-- Overview -->
	<section class="space-y-3">
		<h2 class="text-xl font-semibold text-gray-900">What is this?</h2>
		<p class="text-gray-600">
			Sailboat Market Analyzer helps you navigate the complex process of buying a bluewater sailboat. It combines design data, scoring algorithms, live BoatTrader listings, and AI-powered analysis to help you make informed decisions.
		</p>
		<p class="text-gray-600">
			Buying a cruising sailboat is typically a multi-year process. You might explore different use cases (coastal vs. bluewater vs. circumnavigation), change your preferences as you learn more, and track dozens of listings before finding the right boat. This tool is designed for that journey.
		</p>
	</section>

	<!-- Step 1 -->
	<section class="space-y-3">
		<h2 class="text-xl font-semibold text-gray-900">Step 1: Define Your Use Case</h2>
		<p class="text-gray-600">
			Start by telling us how you plan to sail. Your use case determines how we score each boat design:
		</p>
		<ul class="list-inside list-disc space-y-1 text-gray-600">
			<li><strong>Bluewater passage-making</strong> — ocean crossings, heavy weather capability, self-sufficiency</li>
			<li><strong>Coastal cruising</strong> — shorter hops, marina access, comfort and ease of handling</li>
			<li><strong>Pacific circuit</strong> — the coconut milk run, trade wind sailing, tropical anchoring</li>
			<li><strong>Singlehanded sailing</strong> — manageable sail area, center cockpit, self-steering</li>
			<li><strong>Liveaboard</strong> — interior volume, systems capacity, comfort at anchor</li>
			<li><strong>Circumnavigation</strong> — ultimate durability, range, and versatility</li>
		</ul>
		<p class="text-gray-600">
			Also set your experience level and target waters. These help the AI analysis give you relevant, personalized advice.
		</p>
	</section>

	<!-- Step 2 -->
	<section class="space-y-3">
		<h2 class="text-xl font-semibold text-gray-900">Step 2: Set Your Preferences</h2>
		<p class="text-gray-600">
			Optionally filter designs based on what matters to you:
		</p>
		<ul class="list-inside list-disc space-y-1 text-gray-600">
			<li><strong>Cockpit type</strong> — center cockpit (better for bluewater) vs. aft cockpit (better sailing feel)</li>
			<li><strong>Rig type</strong> — sloop, cutter, ketch, or cutter-ketch. Ketches and cutters offer more flexibility for shorthanding</li>
			<li><strong>Size range</strong> — set a minimum and maximum LOA</li>
			<li><strong>Budget</strong> — filter by maximum purchase price</li>
			<li><strong>Exclusions</strong> — no teak decks (maintenance), no canoe stern (preference), prefer keel-stepped mast (strength)</li>
		</ul>
		<p class="text-gray-600">
			These preferences are saved to your account and carried across sessions.
		</p>
	</section>

	<!-- Step 3 -->
	<section class="space-y-3">
		<h2 class="text-xl font-semibold text-gray-900">Step 3: Explore Designs & Track Listings</h2>
		<p class="text-gray-600">
			Designs are ranked by a composite score based on your use case. Click into any design to see:
		</p>
		<ul class="list-inside list-disc space-y-1 text-gray-600">
			<li><strong>Design specs</strong> — displacement, ballast ratio, D/L ratio, SA/D ratio, capsize screening, motion comfort</li>
			<li><strong>Score radar chart</strong> — visual comparison across all use cases</li>
			<li><strong>Score breakdown</strong> — see exactly which factors contribute to each score and why</li>
			<li><strong>BoatTrader listings</strong> — search live listings with price, location, and specs. Click "Track Listing" to add it to your watchlist</li>
		</ul>
		<p class="text-gray-600">
			Tracked listings are saved to your account and associated with your current session.
		</p>
	</section>

	<!-- Step 4 -->
	<section class="space-y-3">
		<h2 class="text-xl font-semibold text-gray-900">Step 4: Get AI Analysis</h2>
		<p class="text-gray-600">
			Once you've tracked some listings, go to "Your Listings" to generate an analysis prompt. The prompt includes:
		</p>
		<ul class="list-inside list-disc space-y-1 text-gray-600">
			<li>Your buyer profile (use case, experience, target waters)</li>
			<li>Your preferences (cockpit, rig, budget, exclusions)</li>
			<li>Full design specs for each tracked boat</li>
			<li>Score breakdowns showing exactly why each boat scores the way it does</li>
			<li>Listing URLs that the AI can browse for engine hours, rigging condition, photos, and more</li>
		</ul>
		<p class="text-gray-600">
			Copy the prompt and paste it into <a href="https://claude.ai" target="_blank" rel="noopener noreferrer" class="font-medium text-blue-600 hover:text-blue-800">claude.ai</a> (or another AI assistant). You'll get a comprehensive analysis covering:
		</p>
		<ul class="list-inside list-disc space-y-1 text-gray-600">
			<li>Design comparison and suitability ranking</li>
			<li>Price analysis and total cost of ownership</li>
			<li>Refit budget estimates based on age and condition</li>
			<li>Delivery cost estimates for boats in other regions</li>
			<li>Pre-purchase survey focus areas for each design</li>
			<li>Red flags extracted from listing descriptions and photos</li>
		</ul>
	</section>

