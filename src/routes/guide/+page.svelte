	<!-- Understanding scores -->
	<section class="space-y-3">
		<h2 class="text-xl font-semibold text-gray-900">Understanding the Scores</h2>
		<p class="text-gray-600">
			Each design is scored 0–100 across multiple use cases. Scores are computed from design characteristics:
		</p>
		<div class="overflow-x-auto">
			<table class="w-full text-sm">
				<thead>
					<tr class="border-b border-gray-200 text-left">
						<th class="py-2 pr-4 font-medium text-gray-700">Metric</th>
						<th class="py-2 pr-4 font-medium text-gray-700">What it means</th>
						<th class="py-2 font-medium text-gray-700">Good for bluewater</th>
					</tr>
				</thead>
				<tbody class="text-gray-600">
					<tr class="border-b border-gray-100">
						<td class="py-2 pr-4 font-medium">D/L Ratio</td>
						<td class="py-2 pr-4">Displacement-to-length — how heavy the boat is for its size</td>
						<td class="py-2">200+ (heavier = more comfortable offshore)</td>
					</tr>
					<tr class="border-b border-gray-100">
						<td class="py-2 pr-4 font-medium">SA/D Ratio</td>
						<td class="py-2 pr-4">Sail area to displacement — how much sail for the weight</td>
						<td class="py-2">14–18 (enough power without being overpowered)</td>
					</tr>
					<tr class="border-b border-gray-100">
						<td class="py-2 pr-4 font-medium">Capsize Screening</td>
						<td class="py-2 pr-4">Resistance to capsize based on beam and displacement</td>
						<td class="py-2">&lt; 2.0 (lower is more stable)</td>
					</tr>
					<tr class="border-b border-gray-100">
						<td class="py-2 pr-4 font-medium">Motion Comfort</td>
						<td class="py-2 pr-4">Ted Brewer's comfort ratio — how gentle the motion is</td>
						<td class="py-2">30+ (higher is more comfortable)</td>
					</tr>
					<tr>
						<td class="py-2 pr-4 font-medium">Ballast Ratio</td>
						<td class="py-2 pr-4">Percentage of displacement that is ballast</td>
						<td class="py-2">35–45% (more ballast = more stability)</td>
					</tr>
				</tbody>
			</table>
		</div>
		<p class="text-sm text-gray-500">
			Scores also factor in structural characteristics like rudder type, keel type, rig, cockpit layout, and mast step — weighted differently for each use case.
		</p>
	</section>

